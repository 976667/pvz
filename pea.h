/*
 * =============================================================================
 * 文件: pea.h - 豌豆子弹头文件
 * 描述: 定义豌豆子弹类Pea，用于射手类植物发射的攻击物
 *       支持普通豌豆和冰冻豌豆（雪豌豆）两种类型
 *       包含天气系统对子弹速度的影响
 * =============================================================================
 */

#ifndef PEA_H
#define PEA_H

#include "other.h"

// 前向声明，避免循环依赖
class Plant;

/*
 * =============================================================================
 * 类: Pea
 * 继承: Other
 * 功能: 豌豆子弹类，代表射手植物发射的子弹
 *
 * 设计特点:
 * - 单向移动: 从左向右飞行
 * - 碰撞检测: 与僵尸碰撞时造成伤害
 * - 天气影响: 不同天气条件下速度不同
 * - 经验系统: 击杀僵尸后给父植物增加经验
 *
 * 子弹类型:
 * - 普通豌豆: 标准伤害，无特殊效果
 * - 冰冻豌豆: 标准伤害 + 减速效果
 *
 * 核心属性:
 * - atk: 基础攻击力
 * - speed: 飞行速度（像素/帧）
 * - snow: 是否为冰冻豌豆
 * - m_parentPlant: 发射此子弹的父植物（用于经验传递）
 * - m_attackMultiplier: 攻击力倍率（基于父植物等级）
 *
 * 天气速度因子:
 * - Wind(大风): 1.3x 速度
 * - Rain(雨天): 1.0x 速度
 * - Snow(雪天): 0.75x 速度
 * - Sandstorm(沙尘暴): 0.65x 速度
 * =============================================================================
 */
class Pea : public Other
{
public:
    /*
     * =============================================================================
     * 构造函数: Pea
     *
     * 参数:
     *   attack - 基础攻击力（伤害值）
     *   flag - 是否为冰冻豌豆（true=冰冻豌豆，false=普通豌豆）
     *   parentPlant - 发射此子弹的植物指针（用于经验系统，可为nullptr）
     *
     * 功能:
     *   初始化豌豆子弹的所有属性
     *   根据父植物计算攻击力倍率
     * =============================================================================
     */
    Pea(int attack = 0, bool flag = false, Plant *parentPlant = nullptr);

    /*
     * =============================================================================
     * Qt图形项方法
     * =============================================================================
     */

    /*
     * 函数: boundingRect()
     * 功能: 返回子弹的边界矩形
     * 返回: QRectF(-12, -28, 24, 24)
     *       24x24像素的正方形区域
     * 重写: override关键字表示这是从基类继承的方法
     */
    QRectF boundingRect() const override;

    /*
     * 函数: paint()
     * 功能: 绘制豌豆子弹图形
     *       根据snow标志选择不同的图片:
     *       - snow=true: 冰冻豌豆图片
     *       - snow=false: 普通豌豆图片
     * 参数:
     *   painter - 画家设备
     *   option - 样式选项
     *   widget - 父窗口指针
     */
    void paint(QPainter *painter, const QStyleOptionGraphicsItem *option, QWidget *widget) override;

    /*
     * 函数: collidesWithItem()
     * 功能: 碰撞检测，与Zombie类型进行碰撞判定
     * 碰撞条件:
     *   1. other必须是Zombie类型
     *   2. y坐标必须相同（同一条线路）
     *   3. x坐标距离小于15像素
     * 参数:
     *   other - 另一个图形项
     *   mode - 选择模式（此函数未使用）
     * 返回: true表示碰撞，false表示无碰撞
     */
    bool collidesWithItem(const QGraphicsItem *other, Qt::ItemSelectionMode mode) const override;

    /*
     * 函数: advance()
     * 功能: 游戏每帧更新子弹状态
     *       处理移动、碰撞检测和伤害计算
     * 参数:
     *   phase - 前进阶段（phase=0时仅更新位置，phase=1时处理碰撞）
     * 实现:
     *   1. 检查阶段，只有phase=1时才处理逻辑
     *   2. 根据天气调整移动速度
     *   3. 检测与僵尸的碰撞
     *   4. 如果碰撞，对僵尸造成伤害
     *   5. 如果是冰冻豌豆且僵尸未被减速，则减速僵尸
     *   6. 给父植物增加经验
     *   7. 更新子弹位置
     *   8. 如果子弹飞出屏幕右侧，删除子弹
     */
    void advance(int phase) override;

private:
    /*
     * =============================================================================
     * 私有成员变量
     * =============================================================================
     */

    // snow: 冰冻豌豆标志
    // true表示这是冰冻豌豆，可以减速僵尸
    // false表示普通豌豆，只有伤害效果
    bool snow;

    // atk: 基础攻击力
    // 每次命中僵尸造成的伤害值
    // 实际伤害 = atk * m_attackMultiplier
    int atk;

    // speed: 飞行速度（像素/帧）
    // 初始值 = 360.0 * 33 / 1000 ≈ 11.88 像素/帧
    // 将在advance()中根据天气进行调整
    qreal speed;

    // m_parentPlant: 父植物指针
    // 指向发射此子弹的植物
    // 用于将击杀僵尸的经验传递给植物
    // 可为nullptr（如测试时）
    Plant *m_parentPlant;

    // m_attackMultiplier: 攻击力倍率
    // 基于父植物的等级计算
    // 1级植物: 1.0x
    // 2级植物: 1.5x
    // 3级植物: 2.0x
    // 如果没有父植物，默认1.0x
    double m_attackMultiplier;
};

#endif // PEA_H
